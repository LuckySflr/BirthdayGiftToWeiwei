//其他函数
void cal_TH0TL0(uchar refyinchang)
{
	unsigned long int temp1;
	temp1 = refyinchang * 1000;
	temp1 = temp1 * FREQUENCY / NT;
	value_TH0 = (65536 - temp1) / 256;
	value_TL0 = (65536 - temp1) % 256;
}

uint length_array(uchar *ge_name)
{
	uint length = 0;
	uchar *p_ge;
	p_ge = ge_name;
	while (*p_ge != 0xff)
	{
		p_ge++;
		length++;
	}
	return length;
}
//定时器0&1初始化
void Timer0and1_Init(void)
{
	TMOD = 0x11; //Timer0 AND Timer1 mode is 16-bit Timer
	cal_TH0TL0(REF_YINCHANG);
	//Timer0配置
	TH0 = value_TH0;
	TL0 = value_TL0; //25ms中断一次,实际是32ms
	ET0 = 1;

	//Timer1配置
	TH1 = FREQH_high[count1]; //定时时间待定
	TL1 = FREQL_high[count1]; //定时器以beep=1开始
	ET1 = 1;
}


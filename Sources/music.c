void Diaogao(uchar diao_ming) //计算各种调的频率
{
	unsigned char i, j, k;
	unsigned int Ndiao[NUM_FREQUENCY]; //N调频率对应表
	unsigned int N_low, N_high;
	if (diao_ming >= 1 && diao_ming <= 3) //C调或者D/E
	{
		i = diao_ming * 2 + 22;
	}
	else if (diao_ming >= 4 && diao_ming <= 7) //F调或者G/A/B
	{
		i = diao_ming * 2 + 21;
	}
	else if (diao_ming >= 8 && diao_ming <= 9) //sC调或者sD调
	{
		i = diao_ming * 2 + 9;
	}
	else if (diao_ming >= 10 && diao_ming <= 12) //sF调或者sG/sA调
	{
		i = diao_ming * 2 + 10;
	}
	//以Cdiao[i]=中音1位标准
	//计算21标准音
	for (j = 0; j < 3; j++)
	{
		//计算7个低音/中音/高音的标准音
		Ndiao[j * 7] = Cdiao[i + 12 * (j - 1)];			 //1
		Ndiao[j * 7 + 1] = Cdiao[i + 2 + 12 * (j - 1)];	 //2
		Ndiao[j * 7 + 2] = Cdiao[i + 4 + 12 * (j - 1)];	 //3
		Ndiao[j * 7 + 3] = Cdiao[i + 5 + 12 * (j - 1)];	 //4
		Ndiao[j * 7 + 4] = Cdiao[i + 7 + 12 * (j - 1)];	 //5
		Ndiao[j * 7 + 5] = Cdiao[i + 9 + 12 * (j - 1)];	 //6
		Ndiao[j * 7 + 6] = Cdiao[i + 11 + 12 * (j - 1)]; //7

		//计算5个低音/中音/高音的升降音
		Ndiao[j * 5 + 21] = Cdiao[i + 1 + 12 * (j - 1)];	  //s1
		Ndiao[j * 5 + 1 + 21] = Cdiao[i + 3 + 12 * (j - 1)];  //s2
		Ndiao[j * 5 + 2 + 21] = Cdiao[i + 6 + 12 * (j - 1)];  //s4
		Ndiao[j * 5 + 3 + 21] = Cdiao[i + 8 + 12 * (j - 1)];  //s5
		Ndiao[j * 5 + 4 + 21] = Cdiao[i + 10 + 12 * (j - 1)]; //s6
	}
	//2重高音
	Ndiao[36] = Cdiao[i + 24]; //zgy1
	Ndiao[37] = Cdiao[i + 26]; //zgy2

	for (k = 0; k < NUM_FREQUENCY; k++)
	{
		//N=((1000000/(2*Ndiao[k]))*FREQUENCY)/NT;
		//N的计算方法:总的需要定时的时间1000 000/(2*Ndiao[k])(单位：us)  除以每一个机器周期时间NT/FREQUENCY
		//上面是按照50/50的占空比算的

		//如果占空比不是这样子的呢
		N_high = ((1000000) * FREQUENCY / (Ndiao[k] * NT)) * DUTY_HIGH_MUSIC / TOTLE_DUTY_MUSIC;
		N_low = ((1000000) * FREQUENCY / (Ndiao[k] * NT)) * DUTY_LOW_MUSIC / TOTLE_DUTY_MUSIC;
		FREQH_high[k] = (65536 - N_high) / 256;
		FREQL_high[k] = (65536 - N_high) % 256;
		FREQH_low[k] = (65536 - N_low) / 256;
		FREQL_low[k] = (65536 - N_low) % 256;
	}
	//count1计算该gequ的第num_yinfu个音符对应diaogao的频率所应该给计时器加载的初值为频率初值表中的第几个
	count1 = *(gequ + num_yinfu) + 7 * (*(gequ + num_yinfu + 1) - 1) - 1;
}

